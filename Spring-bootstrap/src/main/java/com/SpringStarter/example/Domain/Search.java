package com.SpringStarter.example.Domain;

public class Search {
	private String from;
	private String from_sep;
	private String where;
	private String where_content;
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getFrom_sep() {
		return from_sep;
	}
	public void setFrom_sep(String from_sep) {
		this.from_sep = from_sep;
	}
	public String getWhere() {
		return where;
	}
	public void setWhere(String where) {
		this.where = where;
	}
	public String getWhere_content() {
		return where_content;
	}
	public void setWhere_content(String where_content) {
		this.where_content = where_content;
	}
}
